/**
 * ランダムに特別イベントを取得
 */
export function getRandomSpecialLifeEvent(): SpecialLifeEvent {
  const allEvents = getAllSpecialLifeEvents();
  return allEvents[Math.floor(Math.random() * allEvents.length)];
}

/**
 * カテゴリー別に特別イベントを取得
 */
export function getSpecialLifeEventByCategory(
  category: 'holiday' | 'romance' | 'achievement' | 'cultural' | 'surprise' | 'career_inspiration'
): SpecialLifeEvent {
  let events: SpecialLifeEvent[];

  switch (category) {
    case 'holiday':
      events = holidayEvents;
      break;
    case 'romance':
      events = romanceEvents;
      break;
    case 'achievement':
      events = achievementEvents;
      break;
    case 'cultural':
      events = culturalEvents;
      break;
    case 'surprise':
      events = surpriseEvents;
      break;
    case 'career_inspiration':
      events = careerInspirationEvents;
      break;
  }

  return events[Math.floor(Math.random() * events.length)];
}
